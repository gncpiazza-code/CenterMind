-- 1. Mapeo de Empresas (Nombre en Excel -> ID en CenterMind)
CREATE TABLE IF NOT EXISTS erp_empresa_mapping (
    nombre_erp        TEXT PRIMARY KEY,
    id_distribuidor   BIGINT NOT NULL REFERENCES distribuidores(id_distribuidor)
);

-- 2. Padrón de Clientes (Raw)
CREATE TABLE IF NOT EXISTS erp_clientes_raw (
    id_distribuidor       BIGINT NOT NULL REFERENCES distribuidores(id_distribuidor),
    id_cliente_erp_local  TEXT NOT NULL,
    id_cliente_interno    TEXT,
    nombre_cliente        TEXT,
    nombre_fantasia       TEXT,
    vendedor_erp          TEXT,
    sucursal_erp          TEXT,
    lat                   NUMERIC,
    lon                   NUMERIC,
    forma_pago            TEXT,
    updated_at            TIMESTAMPTZ DEFAULT NOW(),
    
    PRIMARY KEY (id_distribuidor, id_cliente_erp_local)
);

-- 3. Informe de Ventas (Raw)
CREATE TABLE IF NOT EXISTS erp_ventas_raw (
    id_distribuidor    BIGINT NOT NULL REFERENCES distribuidores(id_distribuidor),
    nro_documento      TEXT NOT NULL,
    fecha_factura      DATE NOT NULL,
    codi_cliente       TEXT NOT NULL,
    nomcli             TEXT,
    importe_neto       NUMERIC(15,2) DEFAULT 0,
    importe_final      NUMERIC(15,2) DEFAULT 0,
    unidades           NUMERIC(15,2) DEFAULT 0,
    vendedor_erp       TEXT,
    sucursal_erp       TEXT,
    tipo_documento     TEXT,
    created_at         TIMESTAMPTZ DEFAULT NOW(),
    
    PRIMARY KEY (id_distribuidor, nro_documento)
);

-- 4. Configuración de Alertas de Crédito
CREATE TABLE IF NOT EXISTS erp_config_alertas (
    id_config        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_distribuidor  BIGINT NOT NULL REFERENCES distribuidores(id_distribuidor) UNIQUE,
    limite_dinero    NUMERIC(15,2) DEFAULT 500000,
    limite_cbte      INTEGER DEFAULT 5,
    limite_dias      INTEGER DEFAULT 30,
    activo           BOOLEAN DEFAULT TRUE,
    updated_at       TIMESTAMPTZ DEFAULT NOW()
);

-- 5. Resumen de Deuda consolidada (Capa de Acceso Rápido)
CREATE TABLE IF NOT EXISTS erp_deuda_clientes (
    id_deuda              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_distribuidor       BIGINT NOT NULL REFERENCES distribuidores(id_distribuidor),
    id_cliente_erp_local  TEXT NOT NULL,
    nombre_cliente        TEXT,
    vendedor_erp          TEXT,
    saldo_total           NUMERIC(15,2) DEFAULT 0,
    cant_cbte             INTEGER DEFAULT 0,
    antiguedad_max_dias   INTEGER DEFAULT 0,
    alerta_texto          TEXT,
    fecha_snapshot        TIMESTAMPTZ DEFAULT NOW(),
    
    UNIQUE(id_distribuidor, id_cliente_erp_local)
);

-- Índices para mejorar velocidad
CREATE INDEX IF NOT EXISTS idx_ventas_fecha ON erp_ventas_raw(id_distribuidor, fecha_factura);
CREATE INDEX IF NOT EXISTS idx_ventas_cliente ON erp_ventas_raw(id_distribuidor, codi_cliente);
CREATE INDEX IF NOT EXISTS idx_clientes_erp_mapping ON erp_clientes_raw(id_distribuidor, id_cliente_erp_local);

